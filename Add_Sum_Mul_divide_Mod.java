package basics.programs;

public class Add_Sum_Mul_divide_Mod 
{
    static void add()
    {
    	int no1=10;
    	int no2=20;
    	int sum=no1+no2;
    	System.out.println(sum);
    }
    static void sub()
    {
    	int no1=10;
    	int no2=20;
    	int sub=no2-no1;
    	System.out.println(sub);
    }
    static void mul()
    {
    	int no1=10;
    	int no2=20;
    	int mul=no1*no2;
    	System.out.println(mul);
    }
    static void div()
    {
    	int no1=10;
    	int no2=20;
    	int div=no2/no1;
    	System.out.println(div);
    }
    static void mod()
    {
    	int no1=10;
    	int no2=20;
    	int mod=no2%no1;
    	System.out.println(mod);
    }

	public static void main(String[] args) 
	{
		add();
		sub();
		mul();
		div();
		mod();

	}

}
